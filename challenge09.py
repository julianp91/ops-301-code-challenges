#!/usr/bin/env python3



# If statement using equality condition
a = 1
b = 4

if a == b:
    print("a is equal to b")
elif a < b:
    print("a is less than b")
else:
    print("a is greater than b")


if a != b:
    print("a is not equal to b")
elif a < b:
    print("a is less than b")
else:
    print("a is greater than b")
